/*
   Task Description:
   Write a function called "printFullName."
   Given a first and a last name as input arguments, 
   the function should return a single string with the given first and last names separated by a single space. 
   
   For example:

   var output = printFullName('Joe', 'Smith');
   console.log(output); // Expected output: 'Joe Smith'
*/

function printFullName(firstName, lastName) {
  return firstName + " " + lastName
}

printFullName("Jack", "Wilson")
